def say_hello(name):
    """Says hello to the user

    Args:
    :param name: str: The name of the user
    """
    return f"Hello, {name}!"


def say_goodbye(name):
    """Says goodbye to the user

    Args:
    :param name: str: The name of the user
    """
    return f"Goodbye, {name}!"
